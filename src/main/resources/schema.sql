CREATE SEQUENCE IF NOT EXISTS optout_id_seq;
CREATE TABLE IF NOT EXISTS OPTOUTKEYWORDENTITY
(
    SERIALID INT NOT NULL,
    OPTOUTKEYWORD VARCHAR(100) NOT NULL,
    KEYWORDFREQUENCY INT NOT NULL,
    UPDATEDTIME TIMESTAMP DEFAULT current_timestamp,
    PRIMARY KEY (SERIALID)
);
ALTER SEQUENCE optout_id_seq OWNED BY OPTOUTKEYWORDENTITY.SERIALID;
alter table OPTOUTKEYWORDENTITY alter column SERIALID set default nextval('optout_id_seq');

